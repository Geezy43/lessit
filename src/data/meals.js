export function filterMeals(meals, filters) {
  return meals.filter(meal => {
    if (filters.budget > 0 && meal.priceMin > filters.budget) return false
    if (filters.calories > 0 && meal.calories > filters.calories) return false
    if (filters.categories.length > 0 && !filters.categories.includes(meal.category)) return false
    if (filters.moods.length > 0 && !filters.moods.some(m => meal.moods.includes(m))) return false
    return true
  })
}

export function shuffleMeals(arr) {
  const copy = [...arr]
  for (let i = copy.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [copy[i], copy[j]] = [copy[j], copy[i]]
  }
  return copy
}
